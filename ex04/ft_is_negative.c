#include <unistd.h>

void	ft_is_negative(int n)
{
	char	_ne;
	char	_po;

	_ne = 'N';
	_po = 'P';
	if (n >= 0)
	{
		write(1, &_po, 1);
	}
	else
	{
		write(1, &_ne, 1);
	}
}
